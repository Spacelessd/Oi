#include<bits/stdc++.h>
#define int long long

using namespace std;

const int xi_shu=531192758;
const int mod=1e9+7;

int ans;
char s[6][103];
int dp[6][103];

void dfs(int cur)
{
    if(cur==5)
    {
        memset(dp,0,sizeof(dp));
        for(int i=1;i<=100;i++)
        {
            dp[1][i]=dp[1][i-1];
            if(s[1][i]=='1') dp[1][i]++;
        }
        for(int i=2;i<=5;i++)
        {
            for(int j=1;j<=100;j++)
            {
                dp[i][j]=dp[i][j-1];
                if(s[i][j]=='1') dp[i][j]=(dp[i][j]+dp[i-1][j-1])%mod;
            }
        }
        ans=(ans+dp[5][100])%mod;
        return;
    }
    for(int i=cur;i<=5;i++)
    {
        swap(s[cur],s[i]);
        dfs(cur+1);
        swap(s[cur],s[i]);
    }
}

signed main()
{
    while(cin>>(s[1]+1))
    {
        for(int i=2;i<=5;i++) cin>>(s[i]+1);
        ans=0;
        dfs(1);
        cout<<ans*xi_shu%mod<<endl;
    }
    return 0;
}

